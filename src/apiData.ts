export const formatAuth = (auth: AuthType) => {
  switch (auth) {
    case "NONE": return { label: "No Auth", color: "emerald", icon: "✓" };
    case "Key": return { label: "API Key", color: "amber", icon: "🔑" };
    case "OAuth": return { label: "OAuth", color: "blue", icon: "🔐" };
    case "LOGIN_REQUIRED": return { label: "Login", color: "violet", icon: "👤" };
  }
};

export const formatFreeTier = (tier: ApiEntry["freeTier"]) => {
  switch (tier) {
    case "Free": return { label: "Free Forever", color: "emerald" };
    case "Freemium": return { label: "Freemium", color: "blue" };
    case "Trial": return { label: "Free Trial", color: "amber" };
    case "Paid": return { label: "Paid", color: "slate" };
  }
};
